""""
1 -> (1,1) 1/1
2 -> (1,2) 1/2
3 -> (2,1) 2/1
4 -> (1,3) 3/1
5 -> (2,2) 2/2
6 -> (3,1) 1/3

1 - 1/1
2 - 1/2 2/1 
3 - 3/1 2/2 1/3
4 - 1/4 2/3 3/2 4/1
이런식의 배열이 됨

홀수라인: 분자는 -1 / 분모는 +1
짝수라인: 분자는 +1 / 분모는 -1

짝수라인 / 홀수라인 분모 분자의 규칙을 찾으면 될 것 같은데

"""

import sys
input=sys.stdin.readline

x=int(input())
i=1

while x>i:
    x -= i
    i += 1
    
if i%2==0:
    print("%d/%d" %(x,i-x+1))
else:
    print("%d/%d" %(i-x+1,x))
    